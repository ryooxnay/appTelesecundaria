    private void B_guardarRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_guardarRActionPerformed

         if (txt_correo.getText().length() == 0 || txt_usuR.getText().length() == 0 || txt_pwdR.getText().length() == 0 || 
                 !txt_correo.getText().contains("@") || !txt_correo.getText().contains(".") ) {
            JOptionPane.showMessageDialog(null, "Verifique que su correo sea valido");
        } else {
     String email = (txt_correo.getText());
     String usu = (txt_usuR.getText());
     String pwd = (txt_pwdR.getText());
     
      S.Registrar(txt_correo.getText(), txt_usuR.getText(), txt_pwdR.getText() );
      JOptionPane.showMessageDialog(null, "Usuario Agregado Correctamente");
      ModeloLista.addElement(email);
      ModeloLista.addElement(usu);
      ModeloLista.addElement(pwd);
      
         }
        Login L = new Login();
        this.setVisible(false);
        L.setVisible(true);
    }//GEN-LAST:event_B_guardarRActionPerformed

