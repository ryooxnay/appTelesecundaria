    private void MostrarOlviPwdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MostrarOlviPwdActionPerformed
        
        FileReader fr = null;
        try {
        
        int NLinea = 0;
        int i =0;
        String[] usuarios = null;
        String linea;
        sc = new Scanner(new File("Datos.txt"));
        File  f = new File("Datos.txt");
        fr =  new FileReader(f);
        BufferedReader br = new  BufferedReader(fr);
            try {
                while((linea = br.readLine()) != null){
                    NLinea ++;
                }  
            } catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        usuarios = new String[NLinea];
        while(sc.hasNextLine()){
            usuarios[i++] = sc.nextLine();
        }
        intentos ++;
        email = txt_correoOlvi.getText();
        user =  txt_usuOlvi.getText();
        Seguridad s = new Seguridad();
        s.RecuperarUsuario(usuarios, email, user, pwd, intentos);
        lbl_olviPwd.setText(pwd);
        txt_usuOlvi.setText("");
        txt_correoOlvi.setText("");
       

    } catch (FileNotFoundException ex) {
        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);

    }
        
       // lbl_olviPwd.setText("10Yony10");
    }//GEN-LAST:event_MostrarOlviPwdActionPerformed

